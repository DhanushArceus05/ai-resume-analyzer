const renderAtsCategory = (cursor: ReportCursor, label: string, category: AtsCategoryBreakdown | undefined) => {
  if (!category) {
    cursor.keyValue(label, NOT_AVAILABLE);
    return;
  }
  cursor.keyValue(label, `${safeNumber(category.score)} / ${safeNumber(category.maxScore)}`);
  if (category.reason) {
    cursor.paragraph(category.reason);
  }
  if (category.lostPoints && category.lostPoints.length > 0) {
    cursor.bulletList(category.lostPoints);
  }
  cursor.spacer(6);
};

const rewriteLabelSuffix = (status: RewriteLineItem["status"]): string =>
  status === "already-strong" ? " (Already Strong)" : "";

const renderRewriteLineItems = (cursor: ReportCursor, label: string, items: RewriteLineItem[] | undefined) => {
  if (!items || items.length === 0) {
    cursor.subheading(label);
    cursor.paragraph(NOT_AVAILABLE);
    cursor.spacer(10);
    return;
  }
  items.forEach((item, index) => {
    cursor.originalRewrittenPair(
      `${label} ${index + 1}${rewriteLabelSuffix(item.status)}`,
      item.original,
      item.rewritten,
    );
  });
};

const renderInterviewCategory = (cursor: ReportCursor, label: string, questions: InterviewQuestion[] | undefined) => {
  cursor.subheading(label);
  if (!questions || questions.length === 0) {
    cursor.paragraph(NOT_AVAILABLE);
    cursor.spacer(6);
    return;
  }
  questions.forEach((q, index) => {
    cursor.paragraph(`${index + 1}. (${safeText(q.difficulty)}) ${safeText(q.question)}`);
    cursor.paragraph(`Why it's asked: ${safeText(q.whyAsked)}`);
    cursor.spacer(4);
  });
  cursor.spacer(6);
};

