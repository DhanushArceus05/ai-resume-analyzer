/**
 * Generates and downloads a PDF report from the given resume report.
 * Async because jsPDF itself is loaded on demand here (see the
 * type-only import note above) — awaiting this function is required.
 * Throws/rejects with `PdfGenerationError` if there is no report yet,
 * or if jsPDF itself fails partway through — callers (e.g.
 * DownloadReportButton) are expected to catch this and show an inline
 * error message.
 */
export const generatePdfReport = async (report: LatestResumeReport | null): Promise<void> => {
  if (!report || !report.file) {
    throw new PdfGenerationError("No resume report is available to export yet.");
  }

  try {
    const { jsPDF: JsPdfCtor } = await import("jspdf");
    const doc = new JsPdfCtor({ unit: "pt", format: "a4" });
    const cursor = new ReportCursor(doc);

    // 1. Cover header
    cursor.coverTitle("AI Resume Analyzer Report");
    cursor.spacer(4);
    cursor.divider();

    // 2. Candidate info
    cursor.sectionHeader("Candidate Info");
    cursor.keyValue("Name", safeText(report.parsedResume?.basicInfo.name));
    cursor.keyValue("Email", safeText(report.parsedResume?.basicInfo.email));
    cursor.keyValue("Phone", safeText(report.parsedResume?.basicInfo.phone));
    cursor.keyValue("File name", safeText(report.file?.originalName));
    cursor.keyValue("Generated on", formatGeneratedDate());
    cursor.spacer(10);

    // 3. Resume metrics
    cursor.sectionHeader("Resume Metrics");
    cursor.keyValue("ATS Score", safeNumber(report.ats?.overallScore, " / 100"));
    cursor.keyValue("ATS Label", safeText(report.ats?.label));
    cursor.keyValue("Confidence", safeNumber(report.ats?.confidence, "%"));
    cursor.keyValue("JD Match Score", safeNumber(report.jdMatch?.matchScore, " / 100"));
    cursor.keyValue("JD Match Label", safeText(report.jdMatch?.label));
    cursor.spacer(10);

    // 4. AI Analysis
    cursor.sectionHeader("AI Analysis");
    cursor.subheading("Overall Summary");
    cursor.paragraph(safeText(report.analysis?.overallSummary));
    cursor.spacer(8);
    cursor.subheading("Strengths");
    cursor.bulletList(safeList(report.analysis?.strengths));
    cursor.spacer(8);
    cursor.subheading("Weaknesses");
    cursor.bulletList(safeList(report.analysis?.weaknesses));
    cursor.spacer(8);
    cursor.subheading("Recommendations");
    cursor.bulletList(safeList(report.analysis?.recommendations));
    cursor.spacer(10);

    // 5. ATS Breakdown
    cursor.sectionHeader("ATS Breakdown");
    const breakdown = report.ats?.breakdown;
    renderAtsCategory(cursor, "Resume Completeness", breakdown?.resumeCompleteness);
    renderAtsCategory(cursor, "Skills", breakdown?.skills);
    renderAtsCategory(cursor, "Experience", breakdown?.experience);
    renderAtsCategory(cursor, "Education", breakdown?.education);
    renderAtsCategory(cursor, "Projects", breakdown?.projects);
    renderAtsCategory(cursor, "AI Bonus", breakdown?.aiBonus);

    // 6. JD Match
    cursor.sectionHeader("Job Description Match");
    cursor.subheading("Matched Skills");
    cursor.bulletList(safeList(report.jdMatch?.matchedSkills));
    cursor.spacer(8);
    cursor.subheading("Missing Skills");
    cursor.bulletList(safeList(report.jdMatch?.missingSkills));
    cursor.spacer(8);
    cursor.subheading("Keyword Overlap");
    cursor.bulletList(safeList(report.jdMatch?.keywordOverlap));
    cursor.spacer(8);
    cursor.subheading("Recommendations");
    cursor.bulletList(safeList(report.jdMatch?.recommendations));
    cursor.spacer(10);

    // 7. Resume Rewrite Preview
    cursor.sectionHeader("Resume Rewrite Preview");
    const rewriteSections = report.rewrite?.sections;
    cursor.originalRewrittenPair(
      `Summary${rewriteLabelSuffix(rewriteSections?.summary.status)}`,
      rewriteSections?.summary.original ?? "",
      rewriteSections?.summary.rewritten ?? "",
    );
    renderRewriteLineItems(cursor, "Experience", rewriteSections?.experience);
    renderRewriteLineItems(cursor, "Project", rewriteSections?.projects);

    // 8. Interview Questions
    cursor.sectionHeader("Interview Questions");
    const interview: InterviewQuestionSet | null = report.interview ?? null;
    renderInterviewCategory(cursor, "Technical", interview?.technical);
    renderInterviewCategory(cursor, "Project", interview?.project);
    renderInterviewCategory(cursor, "Behavioral", interview?.behavioral);
    renderInterviewCategory(cursor, "HR", interview?.hr);

    // 9. Footer (added last, applied retroactively to every page)
    cursor.addFootersToAllPages();

    const baseName = (report.file?.originalName || "resume").replace(/\.[^/.]+$/, "");
    doc.save(`${baseName}-analysis-report.pdf`);
  } catch (error) {
    throw new PdfGenerationError(
      error instanceof Error
        ? `Failed to generate the PDF report: ${error.message}`
        : "Failed to generate the PDF report.",
    );
  }
};
